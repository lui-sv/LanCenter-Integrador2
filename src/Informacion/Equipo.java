package Informacion;

public class Equipo {
    int id_equipo, estado_id_equipo;
    String nombre_estado_equipo, nombre_equipo;

    public int getId_equipo() {
        return id_equipo;
    }

    public void setId_equipo(int id_equipo) {
        this.id_equipo = id_equipo;
    }

    public int getEstado_id_equipo() {
        return estado_id_equipo;
    }

    public void setEstado_id_equipo(int estado_id_equipo) {
        this.estado_id_equipo = estado_id_equipo;
    }

    public String getNombre_estado_equipo() {
        return nombre_estado_equipo;
    }

    public void setNombre_estado_equipo(String nombre_estado_equipo) {
        this.nombre_estado_equipo = nombre_estado_equipo;
    }

    public String getNombre_equipo() {
        return nombre_equipo;
    }

    public void setNombre_equipo(String nombre_equipo) {
        this.nombre_equipo = nombre_equipo;
    }
    
    
}
