    public void Listener(){
        // Agregar listener a la tabla para capturar selecciones de fila
        JtableRecepcionista.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (!e.getValueIsAdjusting()) {
                    int selectedRow = JtableRecepcionista.getSelectedRow();
                    if (selectedRow != -1) {
                        // Obtener el ID de usuario de la fila seleccionada
                        int idUsuario = (int) JtableRecepcionista.getValueAt(selectedRow, 0);
                        usu.setId_usuario(idUsuario);
                        
                        // Llamar al método para obtener información del usuario por su ID
                        Re.obtenerInformacionRecepcionista(usu);
                        
                        // Mostrar la información del usuario en los campos de texto
                        if (usu != null) {
                            txtUsuario.setText(usu.getNombre_usuario());
                            txtcontra.setText(usu.getClave_usuario());
                            txtnombres.setText(usu.getNombres_usu());
                            txtapepater.setText(usu.getApellidoPaterno_usu());
                            txtapemater.setText(usu.getApellidoMaterno_usu());
                            txtdirec.setText(usu.getDireccion_usu());
                            txttelef.setText(usu.getTelefono_usu());
                            txtcorreo.setText(usu.getCorreo_usu());
                        } 
                    }
                }
            }
        });
    }
