                usu.setClave_usuario(txtcontra.getText());
                usu.setNombres_usu(txtnombres.getText());
                usu.setApellidoPaterno_usu(txtapepater.getText());
                usu.setApellidoMaterno_usu(txtapemater.getText());
                usu.setDireccion_usu(txtdirec.getText());
                usu.setTelefono_usu(txttelef.getText());
                usu.setCorreo_usu(txtcorreo.getText());
                
                // Llamar al método para actualizar en la base de datos
                boolean actualizado = Re.actualizarRecepcionista(usu);
                
                if (actualizado) {
                    JOptionPane.showMessageDialog(null, "Usuario actualizado correctamente.");
                     Re.CargarTablaRecepcionista(JtableRecepcionista);
                } else {
                    JOptionPane.showMessageDialog(null, "Error al actualizar usuario,revise si selecciono una fila");
                }
                
        
